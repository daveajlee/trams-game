package de.davelee.trams.util;

/**
 * This enum represents the direction of a stop time or journey which can be either outgoing or return.
 * @author Dave Lee
 */
public enum Direction {

    /**
     * Outgoing journey away from first terminus.
     */
    OUTGOING,

    /**
     * Return journey going to first terminus.
     */
    RETURN

}
